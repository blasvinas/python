# How to work with strings

#Print the string, lowercase, uppcase and title
name = "Blas vinas"
print(f'Hello {name}')
print(name.upper())
print(name.lower())
print(name.title())

text = '   Text with spaces at the begining and the end    '
print(text)
print(text.rstrip())
print(text.lstrip())
print(text.strip())